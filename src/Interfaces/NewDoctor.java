    private void labelCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelCrearMouseClicked
        if (lleno()){
            String nombreRegistrado = nombretf.getText();
            String segundoNombreRegistrado = nombretf_dos.getText();
            String apellidoPaternoRegistrado = apaternotf.getText();
            String apellidoMaternoRegistrado = amaternotf.getText();
            String sexoRegistrado;
            if (masculino.isSelected()){
                sexoRegistrado = "1";
            } else {
                sexoRegistrado = "0";
            }
            db.newDoctor(nombreRegistrado, segundoNombreRegistrado, apellidoPaternoRegistrado, apellidoMaternoRegistrado, sexoRegistrado);
            db.desconectar();
            JOptionPane.showMessageDialog(this, "Se ha creado el nuevo doctor");
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos obligatorios","AVISO",1);
        }
    }//GEN-LAST:event_labelCrearMouseClicked

