    public boolean lleno(){
        boolean lleno;
        if (nombretf.getText().equals("") ||
                apaternotf.getText().equals("") ||
                amaternotf.getText().equals("") ||
                (!masculino.isSelected() && !femenino.isSelected())
                ){
            lleno = false;
        } else {
            lleno = true;
        }
        return lleno;
    }

